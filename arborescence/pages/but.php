        <h1>Le BUT MMI</h1>
        <h2>MMI, pluridisciplinaire et professionnalisante</h2>
        <h3>EN BREF</h3>
        <p>
            Le BUT Métiers du multimédia et de l’Internet (MMI) anciennement DUT SRC (Services et réseaux de communication) est une formation universitaire qui forme les étudiants à la conception, à la production d’outils, de services et de contenus de communication numérique. En France, ce sont 31 I.U.T qui proposent ce BUT.
            Cette formation polyvalente du BUT MMI est un diplôme de formation courte de niveau bac+2/+3, axée sur la communication, la culture et la connaissance de l'environnement socio-économique ; la culture technologique et le développement multimédia.
        </p>
        <h3>PROGRAMME ET ORGANISATION</h3>
        <p>La formation se déroule sur 6 semestres et comprend des cours magistraux (CM), des travaux dirigés (TD), des travaux pratiques (TP), et des projets tutorés. Les cours d’un DUT ont lieu dans des Institut Universitaire de Technologie (IUT), au sein des universités. </p>
        <p>
            Les disciplines étudiées durant ce cursus en trois ans sont très variées : communication orale et écrite, intégration web, programmation, infographie, langue vivante, audiovisuel, marketing, réseaux et informatique, théorie de l’information et de la communication, culture générale et communicationnelle, multimédia.
            Afin d’apporter une expérience professionnelle supplémentaire aux étudiants, des périodes de stage en entreprise ont lieu.
        </p>
        <h3>Après une première année de tronc commun, c’est en 2ème année que le choix d’un parcours se fait. </h3>
        <p>
            Le BUT MMI propose 3 parcours :
            <ul>
                <li>stratégie de communication numérique et design d’expérience</li>
                <li>création numérique</li>
                <li>développement web et dispositifs interactifs</li>
            </ul>
            Ce diplôme permet de travailler dans divers secteurs : marketing, culturel, communication, audiovisuel, web…            
        </p>
        <h3>ACCES</h3>
        <p>Le BUT MMI est une formation sélective accessible par la plateforme Parcoursup. Le BUT permet d’accueillir des candidats issus de toutes les séries de baccalauréat ou titre équivalent. L’accès à la formation se fait sur dossier (lettres de motivation, dossier scolaire, CV examinés par un jury d’admission). Certains IUT organisent également des entretiens ou des tests.</p>
